/*
    CONCEITOS FUNDAMENTAIS DA POO

    1. Classe: Uma classe é um modelo ou planta baixa para criar objetos. Define os atributos e métodos comuns a todos os objetos que serão instanciados a partir dela.

    2. Objeto: Um objeto é uma instância de uma classe. É uma entidade que possui características (atributos) e comportamentos (métodos).

    3. Atributo: Também chamado de campo ou propriedade, é uma variável que armazena dados associados a um objeto.

    4. Método: Uma função associada a um objeto que pode ser chamada para realizar operações sobre os dados do objeto.

    5. Encapsulamento: É o princípio de limitar o acesso direto aos detalhes internos de um objeto, protegendo seus atributos e métodos através de modificadores de acesso.

    6. Herança: É um mecanismo que permite que uma classe (subclasse ou classe filha) herde atributos e métodos de outra classe (superclasse ou classe pai).

    7.Polimorfismo: É a capacidade de um objeto se comportar de várias formas. Pode-se ter diferentes classes com métodos de mesmo nome, mas com implementações específicas para cada classe.
*/